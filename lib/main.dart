class MyHomePage extends StatefulWidget {
  const MyHomePage({Key? key, required this.title}) : super(key: key);

  // This widget is the home page of your application. It is stateful, meaning
  // that it has a State object (defined below) that contains fields that affect
  // how it looks.

  // This class is the configuration for the state. It holds the values (in this
  // case the title) provided by the parent (in this case the App widget) and
  // used by the build method of the State. Fields in a Widget subclass are
  // always marked "final".

  final String title;

  @override
  State<MyHomePage> createState() => _MyHomePageState();
}

class _MyHomePageState extends State<MyHomePage> with TickerProviderStateMixin {

  late TabController _tabController;

  @override
  void initState() {
    super.initState();
    _tabController = TabController(length: 2, vsync: this);
  }

  static const List<Tab> _tabs = [
    Tab(icon: Icon(Icons.catching_pokemon), text: Label.pokemons),
    Tab(icon: Icon(Icons.favorite), text: Label.favourite)
  ];

  static const List<Widget> _views = [
    Center(child: PokemonListingPage()),
    Center(child: PokemonListingPage(isFav: true,))
  ];

  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      home: DefaultTabController(
        length: 2,
        child: Scaffold(
          appBar: AppBar(
            title: Text(widget.title),
            backgroundColor: Colours.colorPrimary,
          ),
          body: const TabBarView(
            physics: BouncingScrollPhysics(),
            // Uncomment the line below and remove DefaultTabController if you want to use a custom TabController
            // controller: _tabController,
            children: _views,
          ),
          bottomNavigationBar: const BottomAppBar(
            color: Colours.colorPrimary,
            child: TabBar(
              indicatorColor: Colors.transparent,
              tabs: _tabs,
            ),
          ),
        ),
      ),
    );
  }
}
